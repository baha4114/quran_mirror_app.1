def nuzul(s):
    """شمارهٔ نزولِ سوره (یا 0 اگر نامعتبر)."""
    m = SURAH_META.get(int(s) if s else 0)
    return m[0] if m else 0


def total_ayahs(s):
    """تعداد آیاتِ سوره."""
    m = SURAH_META.get(int(s) if s else 0)
    return m[1] if m else 0


def name(s):
    """نامِ سوره."""
    m = SURAH_META.get(int(s) if s else 0)
    return m[2] if m else '?'


def rev_str(n):
    """بازتابِ رشته‌ای با حفظِ صفرها (لایهٔ عددی): 40→0468… 4→40، 100→001، 110→011.
    مقدارِ رشته‌ای برای تحلیلِ اثرانگشتی نگه داشته می‌شود (04 ≠ 4 ≠ 40)."""
    return str(n).zfill(2)[::-1]


