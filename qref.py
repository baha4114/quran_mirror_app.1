def op_code(mode):
    """از متنِ حالت/عملگر کدِ T1..T7 را حدس می‌زند (یا '' )."""
    s = str(mode or '')
    for c in ('T1','T2','T3','T4','T5','T6','T7'):
        if c in s:
            return c
    return ''


def numeric_fingerprint(S, A, s, a, op=''):
    """دیکشنریِ ویژگی‌های عددیِ یک جفتِ بذر←مقصد برای تحلیل."""
    S,A,s,a = int(S),int(A),int(s),int(a)
    N1,N2 = nuzul(S), nuzul(s)
    TA1,TA2 = total_ayahs(S), total_ayahs(s)
    csum = S+A+s+a
    return {
        'op': op or '',
        'seed': (S,A), 'target': (s,a),
        'same_surah': S==s, 'same_ayah': A==a,
        'd_surah': s-S, 'd_ayah': a-A,
        'nuzul_seed': N1, 'nuzul_target': N2, 'd_nuzul': N2-N1,
        'ta_seed': TA1, 'ta_target': TA2,
        'coord_sum': csum,
        'mod11': (csum % 11 == 0),   # اثرانگشتِ واقعی برای T2/T5
        'mod19': (csum % 19 == 0),   # فرضیهٔ آزمون‌نشده (در داده ضعیف)
        'mod29': (csum % 29 == 0),   # فرضیهٔ آزمون‌نشده (در داده ضعیف)
        # نگاهِ ماتریسی [[S,A],[s,a]]
        'edge_top': S+A, 'edge_bottom': s+a, 'edge_left': S+s, 'edge_right': A+a,
        'diag_main': S+a, 'diag_anti': A+s, 'det': S*a-A*s,
    }


def fingerprint_text(S, A, s, a, op=''):
    """بلوکِ متنیِ کوتاهِ فارسی از اثرانگشتِ عددی — برای تزریق در پرامپت."""
    f = numeric_fingerprint(S, A, s, a, op)
    parts = []
    parts.append('بذر %d:%d (نزول %d، آیات %d) ← مقصد %d:%d (نزول %d، آیات %d)' % (
        S, A, f['nuzul_seed'], f['ta_seed'], s, a, f['nuzul_target'], f['ta_target']))
    parts.append('اختلاف: Δسوره=%+d ، Δآیه=%+d ، Δنزول=%+d' % (f['d_surah'], f['d_ayah'], f['d_nuzul']))
    parts.append('هم‌سوره: %s' % ('بله' if f['same_surah'] else 'خیر'))
    parts.append('جمعِ مختصات S+A+s+a=%d → بخش‌پذیر بر 11: %s' % (f['coord_sum'], 'بله' if f['mod11'] else 'خیر'))
    parts.append('ماتریس: اضلاع(بالا=%d،پایین=%d،چپ=%d،راست=%d) قطرها(اصلی=%d،فرعی=%d) دترمینان=%d' % (
        f['edge_top'], f['edge_bottom'], f['edge_left'], f['edge_right'], f['diag_main'], f['diag_anti'], f['det']))
    if op and op in OP_SIGNATURE:
        parts.append('قانونِ عملگر %s: %s' % (op, OP_SIGNATURE[op]))
    return chr(10).join(parts)


